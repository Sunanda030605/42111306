
name = "sunanda"

# Convert to upper case
upper_name = name.upper()
print(upper_name)

# Convert to lower case
lower_name = name.lower()
print(lower_name)  

# Capitalize the first letter
capitalized_name = name.capitalize()
print(capitalized_name)  

# Replace 's' with 'S'
replaced_name = name.replace('s', 'S')
print(replaced_name)
