# Using + operator
str1 = "HI"
str2 = "Iam Sunanda"
result = str1 + " " + str2
print(result)  

# Using join() method
words = ["Hi", "Iam Sunanda"]
result = " ".join(words)
print(result) 

# Using formatted string literals (f-strings)
str1 = "Hi"
str2 = "Iam Sunanda"
result = f"{str1} {str2}"
print(result)  
